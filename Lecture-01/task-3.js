function CtoF(f) {
    if (typeof f !== "number" || f === undefined) return 'გადმოეცი რიცხვითი პარამეტრი'
    return 'Celsius = ' + parseFloat(((5 / 9) * (f - 32)).toFixed(1)) + ' °C'
}

console.log(CtoF(75));